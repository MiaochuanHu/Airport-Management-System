package model;

/**
 *
 * @author limingxia
 */
public class PieChartItem {

    private String text;
    private Integer value;

    public PieChartItem() {
    }

    public PieChartItem(String text, Integer value) {
        this.text = text;
        this.value = value;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public Integer getValue() {
        return value;
    }

    public void setValue(Integer value) {
        this.value = value;
    }

}
