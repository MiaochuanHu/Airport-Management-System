    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        String keyword = keywordTxt.getText();
        if (StringUtils.isBlank(keyword)) {
            JOptionPane.showMessageDialog(this, "Please input keyword!");
            refreshPanel();
            return;
        }

        // support to search by id & name
        List<Person> searchResult = airportStaff.stream().filter(a
                -> a.getPersonName().equalsIgnoreCase(keyword)
                || a.getAccount().equalsIgnoreCase(keyword)
                || a.getGender().equalsIgnoreCase(keyword)
        ).collect(Collectors.toList());

        populateTable(searchResult);
    }//GEN-LAST:event_searchBtnActionPerformed

    private void personTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_personTableMouseClicked
        int selectedRowIndex = personTable.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, Constant.SELECT_ERROR);
            return;
        }

        DefaultTableModel model = (DefaultTableModel) personTable.getModel();
        selectedStaff = (Person) model.getValueAt(selectedRowIndex, 0);

        personInfoPanel.setVisible(true);
        nameLbl.setText(selectedStaff.getPersonName());
        accountLbl.setText(selectedStaff.getAccount());
        genderLbl.setText(selectedStaff.getGender());
        ageTxt.setText(selectedStaff.getAge() + "");
        roleLbl.setText(selectedStaff.getRoleType());
    }//GEN-LAST:event_personTableMouseClicked

