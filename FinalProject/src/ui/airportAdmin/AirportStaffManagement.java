    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectedRowIndex = personTable.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, Constant.SELECT_ERROR);
            return;
        }

        DefaultTableModel model = (DefaultTableModel) personTable.getModel();
        Person person = (Person) model.getValueAt(selectedRowIndex, 0);

        String age = ageTxt.getText();
        if (StringUtils.isBlank(age) || !CheckUtils.checkAge(Integer.parseInt(age))) {
            JOptionPane.showMessageDialog(this, "Wrong Input!");
            return;
        }
        if (!PersonDao.updatePersonAge(person.getPersonID(), Integer.parseInt(age))) {
            JOptionPane.showMessageDialog(this, Constant.DATABASE_ERROR);
            return;
        }
        JOptionPane.showMessageDialog(this, Constant.SUCC);

        refreshPanel();
    }//GEN-LAST:event_jButton1ActionPerformed

