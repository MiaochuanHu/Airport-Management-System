    private void submitBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitBtnMouseClicked
        String account = emailTxt.getText();
        String oldPassword = new String(opTxt.getPassword());
        String newPassword = new String(npTxt.getPassword());
        String confirmPassword = new String(cpTxt.getPassword());

        // check old password is right
        if (StringUtils.isBlank(oldPassword) || StringUtils.isBlank(account)) {
            JOptionPane.showMessageDialog(this, Constant.BLANKS_ERROR);
            return;
        }

        Person exitedPerson = PersonDao.queryLoginByAccount(account);
        if (ObjectUtils.isEmpty(exitedPerson)) {
            JOptionPane.showMessageDialog(this, "Non-existent Account, Cannot Modify Password!");
            return;
        }
        if (!exitedPerson.getPassword().equals(oldPassword)) {
            JOptionPane.showMessageDialog(this, "Old Password is Wrong, Cannot Modify Password!");
            return;
        }

        if (StringUtils.isBlank(newPassword) || StringUtils.isBlank(confirmPassword)) {
            JOptionPane.showMessageDialog(this, Constant.BLANKS_ERROR);
            return;
        }
        if (!CheckUtils.checkPassword(newPassword)) {
            JOptionPane.showMessageDialog(this, "Please Longer Than 8 character, Only Contain Digit and Letter, At Least one Capital Letter!");
            return;
        }
        if (newPassword.equals(oldPassword)) {
            JOptionPane.showMessageDialog(this, "New Password Cannot Be Same with Old!");
            return;
        }
        if (!newPassword.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "Please Check New Password and Confirm Password!");
            return;
        }

        if (!PersonDao.updatePassword(account, newPassword)) {
            JOptionPane.showMessageDialog(this, Constant.DATABASE_ERROR);
            return;
        }

        JOptionPane.showMessageDialog(this, Constant.SUCC);
        this.dispose();
    }//GEN-LAST:event_submitBtnMouseClicked

