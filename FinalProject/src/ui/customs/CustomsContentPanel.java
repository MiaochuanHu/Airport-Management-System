        ee.setPassportNumber(curPassport.getPassportNumber());
        ee.setWithDanger(dangerCheckBox.isSelected());
        if (!ImmRecDao.insertNewRec(ee)) {
            JOptionPane.showMessageDialog(this, Constant.DATABASE_ERROR);
            return;
        }

        // 2. ticket - isBorderCheck / carryDanger
        if (!TicketDao.updateBorderCheckFlag(thisTicket.getTicketID())
                || !TicketDao.updateDangerFlag(thisTicket.getTicketID(), dangerCheckBox.isSelected())) {
            JOptionPane.showMessageDialog(this, Constant.DATABASE_ERROR);
            return;
        }

        JOptionPane.showMessageDialog(this, Constant.SUCC);
        refreshPanel();
