    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        String status = statusBox.getSelectedItem().toString();
        int terminal = terminalTxt.getSelectedIndex();
        int runway = runwaytxt.getSelectedIndex();

        int idParkingLot = Integer.parseInt(terminal + "" + runway);

        if (!FlightDao.updateFlight(curFlight, status, idParkingLot)) {
            JOptionPane.showMessageDialog(this, Constant.DATABASE_ERROR);
            return;
        }

        JOptionPane.showMessageDialog(this, Constant.SUCC);

        this.dispose();
    }//GEN-LAST:event_submitBtnActionPerformed

