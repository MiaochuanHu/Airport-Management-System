    private void setFieldWithCurFlight() {
        flightNoTxt.setText(curFlight.getFlightNo());
        flightCompanyTxt.setText(curFlight.getFlightCompany().getFcName());
        departTimeTxt.setText(curFlight.getDepartTime());
        arrTimeTxt.setText(curFlight.getArrTime());

        statusBox.setSelectedIndex(curFlight.getStatus());

        if (ObjectUtils.isNotEmpty(curFlight.getParkingLot())) {
            String idParkLot = curFlight.getParkingLot() + "";
            int terminal = idParkLot.charAt(0) - '0';
            terminalTxt.setSelectedIndex(terminal);

            int runway = idParkLot.charAt(1) - '0';
            runwaytxt.setSelectedIndex(runway);
        }
    }
