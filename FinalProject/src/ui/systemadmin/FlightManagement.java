    private void populateTable(List<Flight> flights) {
        DefaultTableModel model = (DefaultTableModel) flightTable.getModel();
        model.setRowCount(0);

        if (ObjectUtils.isEmpty(flights)) {
            return;
        }

        for (Flight f : flights) {

            Object[] row = new Object[5];
            row[0] = f;
            row[1] = f.getFlightCompany().getFcName();
            row[2] = f.getDepartTime();
            row[3] = f.getArrTime();
            row[4] = Constant.FLIGHT_STATUS[f.getStatus()];

            model.addRow(row);
        }
    }

    private void refreshPanel() {
        if (onlyView) {
            alterPanel.setVisible(false);
        }
        keywordTxt.setText("");
        initFlights();
        populateTable(flights);
    }
