    private void viewPieChartMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_viewPieChartMouseClicked
        List<PieChartItem> piecharList = FlightDao.flightStatusStat();

        DefaultPieDataset dpd = new DefaultPieDataset();
        for (PieChartItem pci : piecharList) {
            dpd.setValue(pci.getText(), pci.getValue());
        }

        JFreeChart chart = ChartFactory.createPieChart("All Flight Status ", dpd, true,
                true, false);

        ChartFrame chartFrame = new ChartFrame("", chart);
        chartFrame.pack();
        //chartFrame.setUndecorated(true);
        chartFrame.setLocationRelativeTo(null);
        chartFrame.setVisible(true);
    }//GEN-LAST:event_viewPieChartMouseClicked

