    private void setFieldWithCurFlight() {
        flightNoTxt.setText(curFlight.getFlightNo());

        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm");
        String ddt = curFlight.getDepartTime();
        String adt = curFlight.getArrTime();

        LocalDateTime depatDateTime = LocalDateTime.parse(ddt, dtf);
        LocalDateTime arrDateTime = LocalDateTime.parse(adt, dtf);

        LocalDate deparDate = depatDateTime.toLocalDate();
        LocalTime deparTime = depatDateTime.toLocalTime();
        LocalDate arriDate = arrDateTime.toLocalDate();
        LocalTime arriTime = arrDateTime.toLocalTime();

        departureDate.setDate(deparDate);
        timeDeparture.setTime(deparTime);
        arrDate.setDate(arriDate);
        arrTime.setTime(arriTime);

        cboxSet();
        showSeat();
    }

    public void cboxSet() {
        for (String c : FlightCompanyDao.queryFlightComNames()) {
            flightCompanyBox.addItem(c);
        }
        FlightCompany fc = FlightCompanyDao.queryFlightCompanyNameByFlight(curFlight.getFlightNo());
        flightCompanyBox.setSelectedItem(fc.getFcName());
    }

    public String setNewSeatList() {
        String seatList = curFlight.getSeatStr();
        String[] arr = seatList.split(",", 5);

        String bSeatNo = arr[0];
        String bSeatPrice = arr[1];
        String eSeatNo = arr[2];
        String eSeatPrice = arr[3];
        String seatNo = arr[4];

        bSeatPrice = txtBPrice.getText();
        eSeatPrice = txtEPrice.getText();

        String newSeatList = bSeatNo + "," + bSeatPrice + "," + eSeatNo + "," + eSeatPrice + "," + seatNo;
        return newSeatList;
    }

    public void showSeat() {
        String seatList = curFlight.getSeatStr();
        String[] arr = seatList.split(",", 5);

        String bSeatNo = arr[0];
        String bSeatPrice = arr[1];
        String eSeatNo = arr[2];
        String eSeatPrice = arr[3];

        txtBPrice.setText(bSeatPrice);
        txtEPrice.setText(eSeatPrice);
    }
