    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String flightNo = flightNoTxt.getText();
        String flightCompany = flightCompanyBox.getSelectedItem().toString();
        // for test
        LocalDate departDate = departureDate.getDate();
        LocalTime departTime = timeDeparture.getTime();
        LocalDate arriDate = arrDate.getDate();
        LocalTime arriTime = arrTime.getTime();

        LocalDateTime ddt = LocalDateTime.of(departDate, departTime);
        LocalDateTime adt = LocalDateTime.of(arriDate, arriTime);

        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm");

        String ddate = ddt.format(dtf);
        String adate = adt.format(dtf);

        if (StringUtils.isBlank(flightNo) || StringUtils.isBlank(flightCompany)
                || ObjectUtils.isEmpty(departDate) || ObjectUtils.isEmpty(departTime)
                || ObjectUtils.isEmpty(arriDate) || ObjectUtils.isEmpty(arriTime)) {
            JOptionPane.showMessageDialog(this, BLANKS_ERROR);
            return;
        }
        Flight f = new Flight();
        f.setArrTime(adate);
        f.setDepartTime(ddate);

        f.setFlightNo(flightNo);
        f.setSeatStr(setNewSeatList());

        boolean stats = FlightDao.updateFlightInfo(f, flightCompanyBox.getSelectedItem().toString());

        if (stats) {
            JOptionPane.showMessageDialog(this, "Update Flight Successfully.");
        } else {
            JOptionPane.showMessageDialog(this, "Something Wrong.");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

