    private void initPassportInfo() {
        curPassport = PassportDao.queryPassportNoByPid(curPerson.getPersonID());
        if (Objects.isNull(curPassport)) {
            return;
        }

        nameTxt.setText(curPassport.getLegalName());
        // TODO only for test
        genderBox.setSelectedItem(curPerson.getGender());
        countryBox.setSelectedIndex(Constant.VISA_COUNTRY_LIST.indexOf(curPassport.getVisa()));
        passportNoTxt.setText(curPassport.getPassportNumber());
        birthTxt.setText(curPassport.getBirthday());
        expireDateTxt.setText(curPassport.getExpiryDate());

        // disable some buttons
        passportNoTxt.setEditable(false);
        birthTxt.setEditable(false);
        genderBox.setEnabled(false);
    }
