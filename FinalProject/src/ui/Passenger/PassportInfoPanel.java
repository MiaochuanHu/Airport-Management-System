    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        String legalName = nameTxt.getText();
        String visa = countryBox.getSelectedItem().toString();
        String passportID = passportNoTxt.getText();
        String birthday = birthTxt.getText();
        String expireDay = expireDateTxt.getText();

        Passport passport = new Passport(passportID, legalName, expireDay, visa, birthday);
        String warning = passport.checkField();
        if (StringUtils.isNotBlank(warning)) {
            JOptionPane.showMessageDialog(this, warning);
            return;
        }

        if (ObjectUtils.isEmpty(curPassport)) {
            if (!PassportDao.insertNewOne(passport)) {
                JOptionPane.showMessageDialog(this, DATABASE_ERROR);
                return;
            }

            if (!PassenagerDao.updatePassportID(curPerson.getPersonID(), passportID)) {
                JOptionPane.showMessageDialog(this, DATABASE_ERROR);
                return;
            }
        } else {
            if (!PassportDao.updatePassportnfo(passport)) {
                JOptionPane.showMessageDialog(this, DATABASE_ERROR);
                return;
            }
        }

        JOptionPane.showMessageDialog(this, "Successfully");
    }//GEN-LAST:event_submitBtnActionPerformed

