    private void loginPanelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginPanelMouseClicked

        String userName = userNameLbl.getText();
        String password = new String(this.passwordTxt.getPassword());

        if (StringUtils.isEmpty(userName) || StringUtils.isEmpty(password)) {
            JOptionPane.showMessageDialog(this, "Please fill in all the blanks!");
            return;
        }

        Person loginModel = PersonDao.queryLoginByAccount(userName);
        if (ObjectUtils.isEmpty(loginModel)) {
            JOptionPane.showMessageDialog(this, "Non-existent user, please register or check Info!");
            return;
        }
        if (!loginModel.getPassword().equals(password)) {
            JOptionPane.showMessageDialog(this, "Wrong password!");
            return;
        }

        JOptionPane.showMessageDialog(this, "Sign in successfully!");

        closeSelfOpenNew(loginModel);
    }//GEN-LAST:event_loginPanelMouseClicked

