    private void btnSendCommandActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendCommandActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblMonitor.getSelectedRow();
        if (selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Select a airline to view command");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblMonitor.getModel();
        String flight_number = model.getValueAt(selectedRowIndex, 0).toString();
        
        int command = 0;
        String temp = "";
        
        if (txtTER.getText().equals("") || txtRWY.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Empty Command Not Allowed");
            return;
        }
        
        if (txtTER.getText().equals("HOLDING")){
            temp += "0";
        }else if (txtTER.getText().equals("A")){
            temp += "1";
        }else if (txtTER.getText().equals("B")){
            temp += "2";
        }else {
            temp += "3";
        }
        
        if (txtRWY.getText().equals("HOLDING")){
            temp += "0";
        }else if (txtRWY.getText().equals("14")){
            temp += "1";
        }else if (txtRWY.getText().equals("16")){
            temp += "2";
        }else if (txtRWY.getText().equals("22")){
            temp += "3";
        }else {
            temp += "4";
        }
        
        command = Integer.parseInt(temp);
        
        if (!FlightDao.updateFlightTerminalAndRWY(command, flight_number)) {
            JOptionPane.showMessageDialog(this, DATABASE_ERROR);
            return;
        }else{
            JOptionPane.showMessageDialog(this, "Command send");
            initFlights();
            populateMonitor(flights);
            return;
        }
    }//GEN-LAST:event_btnSendCommandActionPerformed

