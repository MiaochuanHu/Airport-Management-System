    private void populateMonitor(List<Flight> flights) {
        DefaultTableModel model = (DefaultTableModel) tblMonitor.getModel();
        model.setRowCount(0);
        int ParkingLotAndTerminal = 0;
        String info = "";
        String rwy = "";
        String terminal = "";
        
        if (ObjectUtils.isEmpty(flights)) {
            return;
        }

        for (Flight f : flights) {

            Object[] row = new Object[4];
            row[0] = f;
            //row[1] = f.getFlightCompany().getFcName();
            //row[2] = DateUtils.dateToString(f.getDepartureTime());
            //row[3] = DateUtils.dateToString(f.getArrivalTime());
            //row[4] = f.getDuration();
            if (f.getParkingLot() == null){
                ParkingLotAndTerminal = 0;
            } else{
                ParkingLotAndTerminal = f.getParkingLot();
                
            }
            if (f.getStatus() == 0){
                    row[1] = "NORMAL";
                }else if (f.getStatus() == 1){
                    row[1] = "DELAY";
                }else if (f.getStatus() == 2){
                    row[1] = "CANCEL";
                }
            
            info = Integer.toString(ParkingLotAndTerminal);
            if(ParkingLotAndTerminal<10){
                info = "0"+info;
            }
            String[] arrOfStr = info.split("", 2);
            if (Integer.parseInt(arrOfStr[0]) == 0){
                terminal = "HOLDING";
            }
            if (Integer.parseInt(arrOfStr[0]) == 1){
                terminal = "Terminal A";
            }
            if (Integer.parseInt(arrOfStr[0]) == 2){
                terminal = "Terminal B";
            }
            if (Integer.parseInt(arrOfStr[0]) == 3){
               terminal = "Terminal C";
            }   
        
            if (Integer.parseInt(arrOfStr[1]) == 0){
                rwy = "HOLDING";
            }
            if (Integer.parseInt(arrOfStr[1]) == 1){
                rwy = "RWY14";
            }
            if (Integer.parseInt(arrOfStr[1]) == 2){
                rwy = "RWY16";
            }
            if (Integer.parseInt(arrOfStr[1]) == 3){
                rwy = "RWY22";
            }
            if (Integer.parseInt(arrOfStr[1]) == 4){
                rwy = "RWY27";
            }
            row[2] = rwy;
            row[3] = terminal;
            model.addRow(row);
        }
    }
